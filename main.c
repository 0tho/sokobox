static struct termios old, current;

void initTermios () {
  int flags = fcntl(fileno(stdin), F_GETFL, 0);
  fcntl(fileno(stdin), F_SETFL, flags | O_NONBLOCK);

  tcgetattr(fileno(stdin), &old);
  current = old; 
  current.c_lflag &= ~(ECHO | ICANON);
  tcsetattr(fileno(stdin), TCSAFLUSH, &current);
}


void resetTermios (void) {
  tcsetattr(fileno(stdin), TCSAFLUSH, &old);
}

char getch () {
  int i = 0;
  char * buff = (char *) malloc(3 * sizeof(char));

  buff[0] = '\0';
  buff[1] = '\0';
  buff[2] = '\0';

  char ch = '\0';
  ssize_t n = 0;

  while (n < 1) {
    n = read(fileno(stdin), &ch, 1);
  }

  while(i < 3 && n > 0) {
    /*printf("reading\n");*/
    buff[i++] = ch;
    n = read(fileno(stdin), &ch, 1);
  }

  if (buff[0] != '\033') {
    ch = buff[0];
  } else {
    if (buff[0] == '\0') {
      ch = '\033';
    } else {
      switch (buff[2]) {
        case 'A':
          ch = UP;
          break;
        case 'B':
          ch = DOWN;
          break;
        case 'C':
          ch = RIGHT;
          break;
        case 'D':
          ch = LEFT;
          break;
      }
    }
  }

  free(buff);
  return ch;
}

